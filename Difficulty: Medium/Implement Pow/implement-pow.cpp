class Solution {
  public:
    double power(double b, int e) {
        double s=1;
        double e1=e;
        e=abs(e);
        while(e>0){
            if(e&1) s=s*b;
            b=b*b;
            e=e/2;
            
        }
        return e1<0?1.0/s:s;
    }
};

